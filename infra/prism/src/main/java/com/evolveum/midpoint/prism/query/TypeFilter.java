package com.evolveum.midpoint.prism.query;

import com.evolveum.midpoint.prism.Objectable;
import com.evolveum.midpoint.prism.PrismObject;
import com.evolveum.midpoint.prism.match.MatchingRuleRegistry;

import javax.xml.namespace.QName;

/**
 * @author lazyman
 */
public class TypeFilter extends ObjectFilter {

    private QName type;

    public TypeFilter(QName type) {
        this.type = type;
    }

    public QName getType() {
        return type;
    }

    @Override
    public ObjectFilter clone() {
        return new TypeFilter(type);
    }

    @Override
    public <T extends Objectable> boolean match(PrismObject<T> object, MatchingRuleRegistry matchingRuleRegistry) {
        return false;
    }

    @Override
    public String debugDump() {
        return debugDump(0);
    }

    @Override
    public String debugDump(int indent) {
        StringBuilder sb = new StringBuilder();
        sb.append("TYPE: ");
        sb.append(type.getLocalPart());

        return sb.toString();
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        TypeFilter that = (TypeFilter) o;

        if (type != null ? !type.equals(that.type) : that.type != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        return type != null ? type.hashCode() : 0;
    }

    @Override
    public String toString() {
        return debugDump();
    }
}
